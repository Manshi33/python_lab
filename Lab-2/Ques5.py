# compute a) sin of 60 degrees b) cos of pi c) sin of (0.8660254037844386) d) tan of 90 degrees

# a)
import math
sin = math.sin(math.radians(60))
print("The sin of 60 degrees is: ", sin)

# b)
cos = math.cos(math.pi)
print("The cos of pi is: ", cos)

# c)
sin = math.sin(0.8660254037844386)
print("The sin of 0.8660254037844386 is: ", sin)
 # d)
tan = math.tan(math.radians(90))
print("The tan of 90 degrees is: ", tan)
